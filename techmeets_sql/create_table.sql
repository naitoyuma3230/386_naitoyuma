drop table if exists EMPLOYEE;
drop table if exists ASSIGNMENT;
drop table if exists LANGSKILL;
drop table if exists GENDER;
drop table if exists DEPARTMENT;
drop table if exists PREFECTURE;
drop table if exists LANGUAGE;


CREATE TABLE EMPLOYEE (
EMPNO VARCHAR(7) NOT NULL COMMENT '社員番号'
,EMPNAME VARCHAR(30) NOT NULL COMMENT '氏名'
,GENDERCD VARCHAR(1) NOT NULL COMMENT '性別CD'
,BIRTHDAY TIMESTAMP NOT NULL COMMENT '生年月日'
,BIRTHPLACE VARCHAR(2) COMMENT '出身CD'
,PRIMARY KEY( EMPNO )
);
CREATE TABLE ASSIGNMENT (
EMPNO VARCHAR(7) NOT NULL COMMENT '社員番号'
,HIREDATE TIMESTAMP NOT NULL COMMENT '入社日'
,DEPTCD VARCHAR(2) NOT NULL COMMENT '所属部門CD'
,SALARY INTEGER NOT NULL COMMENT '給与'
,PRIMARY KEY( EMPNO, DEPTCD )
);
CREATE TABLE LANGSKILL (
EMPNO VARCHAR(7) NOT NULL COMMENT '社員番号'
,LANGCD VARCHAR(2) NOT NULL COMMENT '語学CD'
,PRIMARY KEY( EMPNO, LANGCD )
);
CREATE TABLE GENDER (
GENDERCD VARCHAR(1) NOT NULL COMMENT '性別CD'
,GENDERNAME VARCHAR(5) NOT NULL COMMENT '性別名'
,PRIMARY KEY( GENDERCD )
);
CREATE TABLE DEPARTMENT (
DEPTCD VARCHAR(2) NOT NULL COMMENT '部門CD'
,DEPTNAME VARCHAR(30) NOT NULL COMMENT '部門名'
,EMPNO VARCHAR(7) COMMENT '部門長番号'
,PRIMARY KEY( DEPTCD )
);
CREATE TABLE PREFECTURE (
PREFCD VARCHAR(2) NOT NULL COMMENT '都道府県CD'
,PREFNAME VARCHAR(30) NOT NULL COMMENT '都道府県名'
,REGION VARCHAR(30) COMMENT '地域名'
,PRIMARY KEY( PREFCD )
);
CREATE TABLE LANGUAGE (
LANGCD VARCHAR(2) NOT NULL COMMENT '語学CD'
,LANGNAME VARCHAR(30) NOT NULL COMMENT '語学名'
,PRIMARY KEY( LANGCD )
);

